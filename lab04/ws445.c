    printf ("0x00 | 0x00 : %x\n",0x00 | 0x00);
    printf ("0x00 | 0xFF : %x\n",0x00 | 0xFF);
    printf ("0xFF | 0x00 : %x\n",0xFF | 0x00);
    printf ("0xFF | 0xFF : %x\n",0xFF | 0xFF);
